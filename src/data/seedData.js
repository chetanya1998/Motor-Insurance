const STEP_DURATIONS = {
  vehicle: 18,
  policy: 12,
  estimate: 9,
  contact: 15,
  quotes: 18,
  purchase: 22,
};

function minutesAgo(minutes) {
  return new Date(Date.now() - minutes * 60 * 1000).toISOString();
}

function buildSession(overrides) {
  return {
    sessionId: overrides.sessionId,
    leadId: overrides.leadId ?? null,
    registrationNumber: overrides.registrationNumber ?? "",
    createdAt: overrides.createdAt,
    lastActivityAt: overrides.lastActivityAt,
    status: overrides.status,
    currentScreen: overrides.currentScreen,
    currentStage: overrides.currentStage,
    vehicleLookupMode: overrides.vehicleLookupMode ?? "lookup",
    vehicleLookupStatus: overrides.vehicleLookupStatus ?? "success",
    vehicleLookupConfidence: overrides.vehicleLookupConfidence ?? "high",
    data: overrides.data,
    analytics: {
      validationErrors: overrides.validationErrors ?? 0,
      pasteCount: overrides.pasteCount ?? 0,
      uncertainFields: overrides.uncertainFields ?? [],
      vehicleEditCount: overrides.vehicleEditCount ?? 0,
      honeypotFilled: overrides.honeypotFilled ?? false,
      startedFrom: overrides.startedFrom ?? "cta",
    },
    timing: {
      appLoadedAt: overrides.createdAt,
      journeyStartedAt: overrides.journeyStartedAt,
      currentStageStartedAt: overrides.lastActivityAt,
      stepDurations: overrides.stepDurations ?? STEP_DURATIONS,
      checkpoints: overrides.checkpoints,
    },
  };
}

function buildLead(overrides) {
  return {
    leadId: overrides.leadId,
    sessionId: overrides.sessionId,
    registrationNumber: overrides.registrationNumber,
    createdAt: overrides.createdAt,
    updatedAt: overrides.updatedAt,
    status: overrides.status,
    vehicle: overrides.vehicle,
    policy: overrides.policy,
    customer: overrides.customer,
    purchase: overrides.purchase,
    quoteEstimate: overrides.quoteEstimate,
    exactQuotes: overrides.exactQuotes,
    selectedAddons: overrides.selectedAddons,
    selectedPlan: overrides.selectedPlan,
    score: overrides.score,
    timings: overrides.timings,
  };
}

function buildEvent({
  eventId,
  sessionId,
  leadId = null,
  eventName,
  stepName,
  timestamp,
  timeSinceStart,
  metadata = {},
}) {
  return {
    eventId,
    sessionId,
    leadId,
    eventName,
    stepName,
    timestamp,
    timeSinceStart,
    metadata,
  };
}

